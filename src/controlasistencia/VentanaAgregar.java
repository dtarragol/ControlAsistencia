    private void btn_1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_1ActionPerformed
        Metodos ma=new Metodos();
        int dni = Integer.parseInt(txtDni.getText());
        int edad = Integer.parseInt(txtEdad.getText());
        String nombre = txtName.getText();
        String linea= nombre +";"+dni+";"+edad;
        
        ma.escribirArchivo("archivo.txt", linea);
        ma.agregarAlumno(nombre, dni, edad);
        
        ma.crearArchivo("alumnos/"+nombre+".txt");
        ma.escribirArchivo("alumnos/"+nombre+".txt", "");
        
        JFrame jFrame = new JFrame();
        Icon m = new ImageIcon(getClass().getResource("/ICONO1.png"));
        JOptionPane.showMessageDialog(jFrame, "Alumno agregado correctamente.","Mensaje",JOptionPane.INFORMATION_MESSAGE, m);
    }//GEN-LAST:event_btn_1ActionPerformed

